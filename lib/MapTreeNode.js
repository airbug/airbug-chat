//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('./Class');
var Map = require('./Map');
var Obj = require('./Obj');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var MapTreeNode = Class.extend(Obj, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(data) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        this.data = data;

        this.childNodes = new Map();
    },



    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    getData: function() {
        return this.data;
    },

    setData: function(data) {
        this.data = data;
    },


    //-------------------------------------------------------------------------------
    // Class methods
    //-------------------------------------------------------------------------------

    getChildNode: function(key) {
        return this.childNodes.get(key);
    },

    getNumberOfChildNodes: function () {
        return this.childNodes.getCount();
    },

    putChildNode: function(key, child) {
        this.childNodes.put(key, child);
    },

    removeChildNode: function(key) {
        this.childNodes.remove(key);
    }
});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = MapTreeNode;
